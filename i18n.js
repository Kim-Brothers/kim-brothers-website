const langNames = {
  ko: "한국어", en: "English", zh: "中文", ja: "日本語", es: "Español",
  fr: "Français", de: "Deutsch", pt: "Português", ru: "Русский", ar: "العربية"
};

const rtlLangs = ['ar'];

function detectLang() {
  const saved = localStorage.getItem('lang');
  if (saved && translations[saved]) return saved;

  const nav = navigator.language || navigator.userLanguage || 'ko';
  const short = nav.slice(0, 2).toLowerCase();
  return translations[short] ? short : 'ko';
}

function applyLang(lang) {
  const t = translations[lang];
  if (!t) return;

  document.documentElement.lang = lang;
  document.documentElement.dir = rtlLangs.includes(lang) ? 'rtl' : 'ltr';

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key]) el.innerHTML = t[key];
  });

  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.dataset.i18nPh;
    if (t[key]) el.placeholder = t[key];
  });

  const selector = document.getElementById('lang-current');
  if (selector) selector.textContent = langNames[lang];

  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.classList.toggle('active', opt.dataset.lang === lang);
  });

  localStorage.setItem('lang', lang);
}

function initI18n() {
  const lang = detectLang();
  applyLang(lang);

  document.addEventListener('click', e => {
    const opt = e.target.closest('.lang-option');
    if (opt) {
      applyLang(opt.dataset.lang);
      document.getElementById('lang-dropdown').classList.remove('open');
      return;
    }

    const toggle = e.target.closest('.lang-toggle');
    if (toggle) {
      document.getElementById('lang-dropdown').classList.toggle('open');
      return;
    }

    const dd = document.getElementById('lang-dropdown');
    if (dd) dd.classList.remove('open');
  });
}

document.addEventListener('DOMContentLoaded', initI18n);
